    def get_monthly_co2_mlo(self, year, month):
        """
        Retrieve the monthly CO2 value for a given year and month.

        Parameters:
        year (int): The year for which CO2 data is requested.
        month (int): The month for which CO2 data is requested.
        syear (int): The earliest year for which CO2 data is available.
        smonth (int): The earliest month for which CO2 data is available.
        eyear (int): The latest year for which CO2 data is available.
        emonth (int): The latest month for which CO2 data is available.
        co2mlo (dict): A dictionary containing CO2 data with (year, month) as keys.

        Returns:
        float: The CO2 value for the requested year and month.
        """
        if year < self.syear or (year == self.syear and month < 3):
            print("Warning: Requested CO2 data beyond the earliest record!")
            print("Set to the earliest one...")
            return self.co2mlo[self.syear, self.smonth]

        if year > self.eyear or (year == self.eyear and month > 4):
            print("Warning: Requested CO2 data beyond the latest record!")
            print("Set to the latest one...")
            return self.co2mlo[self.eyear, self.emonth]

        return self.co2mlo[year-self.syear, month-self.smonth]